package be.vdab.util;

public class IntLongUtils {
	public static boolean isStrictPositief(int getal) {
		return getal > 0;
	}
	
	public static boolean isStrictPositief(long getal) {
		return getal > 0;
	}
	
	public static boolean isNietNegatief(int getal) {
		return getal >=0;
	}
	
	public static boolean isNietNegatief(long getal) {
		return getal >= 0;
	}
}
